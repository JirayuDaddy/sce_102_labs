#include <stdio.h>


int main()
{
    int burger = 67;
    float price = 56.52;
    char iname = 'S';
    printf("burger: %d\n", burger);
    printf("burger (hex): %x\n", burger);
    printf("burger (hex-upper): %X\n", burger);
    printf("price: %f\n", price);
    printf("iname: %c\n", iname);
    return 0;
    
}