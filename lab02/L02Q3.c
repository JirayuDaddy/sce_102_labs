#include <stdio.h>
#include <stdlib.h>

int main()
{
    int number;
    printf("Enter number:");
    scanf("%d", &number);
    printf("%d*1 = %d\n",number, number*1);
    printf("%d*2 = %d\n",number, number*2);
    printf("%d*3 = %d\n",number, number*3);
    printf("%d*4 = %d\n",number, number*4);
    printf("%d*5 = %d\n",number, number*5);
    printf("%d*6 = %d\n",number, number*6);
}